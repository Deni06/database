    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtNIS.getText()) || "".equals(txtAlamat.getText()) ||
                "".equals(txtKelas.getText()) || "".equals(txtNama.getText()) || "".equals(txtEmail.getText()) || "".equals(txtAgama.getText()) || "".equals(txtAsal.getText()) || "".equals(txtTelepon.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else {
            String JK = "";
            if (rdLaki.isSelected()) {
                JK = "L";
            }else {
                JK = "P";
            }
            String SQL = "INSERT INTO t_siswa (NIS,NamaSiswa,JenisKelamin,Kelas,Email,Agama,Asal,Telepon,Alamat) "
                    + "VALUES('"+txtNIS.getText()+"','"+txtNama.getText()+"','"+JK+"',"
                    +"'"+txtKelas.getText()+"','"+txtEmail.getText()+"','"+txtAgama.getText()+"','"+txtAsal.getText()+"','"+txtTelepon.getText()+"','"+txtAlamat.getText()+"')";
              int status = KoneksiDB.execute(SQL);
              if (status == 1){
                  JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                  selectData();
              }else{
                  JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
              }
        } 
        
    }//GEN-LAST:event_btnAddActionPerformed

